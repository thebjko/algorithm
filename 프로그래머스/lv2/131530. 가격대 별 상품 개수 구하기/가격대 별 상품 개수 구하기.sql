# SELECT 10000*FLOOR(PRICE/10000)-10000 PRICE_GROUP, COUNT(*) PRODUCTS
# FROM PRODUCT
# GROUP BY 1
# ORDER BY 1;

SELECT  TRUNCATE((price / 10000),0) * 10000 AS PRICE_GROUP , COUNT(PRODUCT_ID) AS PRODUCTS
FROM PRODUCT
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP ASC